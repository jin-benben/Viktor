import { querySingleRule } from '../service';

export default {
  namespace: 'TI_Z02802',
  state: {
    purchaseDetail: {},
  },
  effects: {
    *fetch({ payload }, { call, put }) {
      const response = yield call(querySingleRule, payload);
      if (response && response.Status === 200) {
        yield put({
          type: 'save',
          payload: {
            purchaseDetail: response.Content,
          },
        });
      }
    },
  },
  reducers: {
    save(state, action) {
      return {
        ...state,
        ...action.payload,
      };
    },
  },
};
